import OpenAI from "openai";

const client = new OpenAI({
  apiKey: process.env.GROQ_API_KEY,
  baseURL: "https://api.groq.com/openai/v1"
});

export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Метод не поддерживается" });
  }

  const { answer } = req.body;

  try {
    const response = await client.responses.create({
      model: "openai/gpt-oss-20b",
      input: `Ты — профессиональный психолог. Проведи глубокий анализ на основе ответов на 160 вопросов. 
Не додумывай факты, не выставляй диагнозы, только гипотезы. При признаках опасного поведения — отметь и рекомендуй очную помощь.
Отчёт должен быть строго текстовым, без Markdown, таблиц, эмодзи и символов типа #, *, |, ---.
Структура:
1. Личностный профиль: ключевые черты (4–6) с выдержками из ответов.
2. Эмоциональная стабильность: общая картина, механизмы регуляции, риски с примерами.
3. Особенности общения: стиль, комфортные условия, рекомендации.
4. Мотивация: основные драйверы и проявления.
5. Сильные стороны (3–6) с подтверждениями.
6. Зоны трудностей (3–6) с механизмами и примерами.
7. Рекомендации для развития: конкретные шаги на 2–4 недели, упражнения с форматом, частотой и длительностью.
8. Уточняющие гипотезы и вопросы: 2–4 гипотезы, 1–3 уточняющих вопроса (1–2 предложения).
9. Заключение: 1–2 предложения.

Используй ответы как единый профиль, ищи повторяющиеся темы, действия, эмоции. Цитируй выдержки до 12 слов для доказательства выводов. Если данных недостаточно — добавь секцию «Данные отсутствуют».
Вот ответы:\n${answer}`
    });

    // Берём первый блок текста из ответа
    let cleanText = response.output_text || "";
    cleanText = cleanText.replace(/(\*\*|__|###|---|\|)/g, '');
    cleanText = cleanText.replace(/\n{2,}/g, '\n\n');

    res.status(200).json({ analysis: cleanText });

  } catch (err) {
    console.error(err);
    res.status(500).json({ error: "Ошибка при анализе" });
  }
}
